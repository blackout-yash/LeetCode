class Solution {
  public:
    int maxDepth(string s) {
        int ans = 0, curr = 0;
        for(auto x: s) {
            if(x == '(') curr++;
            else if(x == ')') curr--;
            ans = max(ans, curr);
        }
        return ans;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        string s;
        cin >> s;

        Solution obj;
        int ans = obj.maxDepth(s);
        cout << ans << endl;
    }
    return 0;
}
